async function normalizeRooms(client) {
  const rows = (await client.query("SELECT idroom, stage_state, room_owner_meta FROM rooms ORDER BY idroom")).rows;
  let stageRows = 0;
  let badgeAssignments = 0;

  for (const room of rows) {
    const stageState = parseJson(room.stage_state, {});
    for (const [stage, state] of Object.entries(stageState)) {
      await insertJson(
        client,
        `INSERT INTO room_stage_state (room_idroom, stage_number, state, raw)
         VALUES ($1, $2, $3, $4::jsonb)
         ON CONFLICT (room_idroom, stage_number) DO UPDATE SET state = EXCLUDED.state, raw = EXCLUDED.raw`,
        [room.idroom, Number(stage), Number(state || 0), { source: "rooms.stage_state" }],
      );
      stageRows += 1;
    }

    const ownerMeta = parseJson(room.room_owner_meta, {});
    if (ownerMeta && Object.keys(ownerMeta).length > 0) {
      const ownerId = String(ownerMeta.ownerId || "").replace(/^#/, "");
      await insertJson(
        client,
        `INSERT INTO room_badge_assignments
          (room_idroom, assignment_type, user_idreg, legacy_user_id, username, topic, badge_legacy_id, raw)
         VALUES ($1, 'owner', $2, $3, $4, $5, $6, $7::jsonb)`,
        [
          room.idroom,
          /^\d+$/.test(ownerId) ? Number(ownerId) : null,
          String(ownerMeta.ownerId || ""),
          String(ownerMeta.ownerUser || ""),
          String(ownerMeta.ownerTopic || ""),
          String(ownerMeta.badge_id || ""),
          ownerMeta,
        ],
      );
      badgeAssignments += 1;
    }

    const assignments = Array.isArray(ownerMeta.assignments) ? ownerMeta.assignments : [];
    for (const assignment of assignments) {
      const userId = String(assignment.userId || "").replace(/^#/, "");
      await insertJson(
        client,
        `INSERT INTO room_badge_assignments
          (room_idroom, assignment_type, user_idreg, legacy_user_id, username, topic, badge_legacy_id, raw)
         VALUES ($1, 'assignment', $2, $3, $4, $5, $6, $7::jsonb)`,
        [
          room.idroom,
          /^\d+$/.test(userId) ? Number(userId) : null,
          String(assignment.userId || ""),
          String(assignment.userUser || ""),
          String(assignment.userTopic || ""),
          String(assignment.badge_id || ""),
          assignment,
        ],
      );
      badgeAssignments += 1;
    }
  }

  return { stageRows, badgeAssignments };
}

