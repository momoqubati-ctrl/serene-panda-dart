async function insertJson(client, sql, params) {
  return client.query(sql, params.map((value) => {
    if (value && typeof value === "object" && !Array.isArray(value)) return JSON.stringify(value);
    if (Array.isArray(value)) return JSON.stringify(value);
    return value;
  }));
}

async function normalizeBars(client) {
  const result = await client.query("SELECT id, bid, bcc, likes FROM bars ORDER BY id");
  let comments = 0;
  let replies = 0;
  let likes = 0;

  for (const row of result.rows) {
    const commentItems = parseJson(row.bcc, []);
    if (Array.isArray(commentItems)) {
      for (const comment of commentItems) {
        const inserted = await insertJson(
          client,
          `INSERT INTO bar_comments (bar_id, legacy_bid, author_topic, author_pic, body, raw)
           VALUES ($1, $2, $3, $4, $5, $6::jsonb)
           RETURNING id`,
          [
            row.id,
            String(comment.bid || row.bid || ""),
            String(comment.topic || ""),
            String(comment.pic || ""),
            String(comment.pccus || comment.body || ""),
            comment,
          ],
        );
        comments += 1;
        const commentId = inserted.rows[0].id;
        const replyItems = Array.isArray(comment.replies) ? comment.replies : [];
        for (const reply of replyItems) {
          await insertJson(
            client,
            `INSERT INTO bar_comment_replies (comment_id, legacy_cid, author_topic, author_pic, body, raw)
             VALUES ($1, $2, $3, $4, $5, $6::jsonb)`,
            [
              commentId,
              String(reply.cid || reply.id || ""),
              String(reply.topic || ""),
              String(reply.pic || ""),
              String(reply.pccus || reply.body || ""),
              reply,
            ],
          );
          replies += 1;
        }
      }
    }

    const likeItems = parseJson(row.likes, []);
    if (Array.isArray(likeItems)) {
      for (const like of likeItems) {
        const identity = String(like.identity || like.uid || like.lid || like.idreg || like.id || "");
        if (!identity) continue;
        await insertJson(
          client,
          `INSERT INTO bar_likes (bar_id, identity, uid, lid, idreg, state, raw)
           VALUES ($1, $2, $3, $4, $5, $6, $7::jsonb)
           ON CONFLICT (bar_id, identity) DO UPDATE SET
             uid = EXCLUDED.uid,
             lid = EXCLUDED.lid,
             idreg = EXCLUDED.idreg,
             state = EXCLUDED.state,
             raw = EXCLUDED.raw`,
          [
            row.id,
            identity,
            String(like.uid || ""),
            String(like.lid || ""),
            String(like.idreg || like.id || ""),
            Number(like.state || 0),
            like,
          ],
        );
        likes += 1;
      }
    }
  }

  return { comments, replies, likes };
}

async function normalizeSettings(client) {
  const rows = (await client.query("SELECT * FROM settings ORDER BY id")).rows;
  let settings = 0;
  let assets = 0;
  let categories = 0;
  let gifts = 0;
  let siteBadges = 0;
  let roomBadges = 0;

  const assetFields = [
    ["dro3", "profile_frame", "/dro3"],
    ["them", "theme", "/them"],
    ["cover", "cover", "/cover"],
    ["emo", "emoji", "/emo"],
    ["sico", "profile_icon", "/sico"],
  ];

  for (const row of rows) {
    const site = parseJson(row.site, {});
    await insertJson(
      client,
      `INSERT INTO site_settings (key, value, source)
       VALUES ('legacy.site', $1::jsonb, 'settings.site')
       ON CONFLICT (key) DO UPDATE SET value = EXCLUDED.value, source = EXCLUDED.source, updated_at = CURRENT_TIMESTAMP`,
      [site],
    );
    settings += 1;

    for (const [field, type, prefix] of assetFields) {
      const files = parseJson(row[field], []);
      if (!Array.isArray(files)) continue;
      for (const [index, file] of files.entries()) {
        await insertJson(
          client,
          `INSERT INTO site_assets (asset_type, file_name, file_url, sort_order, raw)
           VALUES ($1, $2, $3, $4, $5::jsonb)
           ON CONFLICT (asset_type, file_name) DO UPDATE SET file_url = EXCLUDED.file_url, sort_order = EXCLUDED.sort_order, raw = EXCLUDED.raw`,
          [type, String(file || ""), normalizePath(file, prefix), index, { source: `settings.${field}`, file }],
        );
        assets += 1;
      }
    }

    const giftCategories = parseJson(row.gift_categories, []);
    if (Array.isArray(giftCategories)) {
      await insertJson(
        client,
        `INSERT INTO site_settings (key, value, source)
         VALUES ('legacy.gift_categories', $1::jsonb, 'settings.gift_categories')
         ON CONFLICT (key) DO UPDATE SET value = EXCLUDED.value, source = EXCLUDED.source, updated_at = CURRENT_TIMESTAMP`,
        [{ categories: giftCategories }],
      );
      settings += 1;
      for (const [index, name] of giftCategories.entries()) {
        await client.query(
          `INSERT INTO gift_categories (name, sort_order)
           VALUES ($1, $2)
           ON CONFLICT (name) DO UPDATE SET sort_order = EXCLUDED.sort_order`,
          [String(name || ""), index],
        );
        categories += 1;
      }
    }

    const giftItems = parseJson(row.gifts_git, []);
    if (Array.isArray(giftItems)) {
      for (const gift of giftItems) {
        await insertJson(
          client,
          `INSERT INTO gifts (legacy_id, name, file_name, file_url, coin, category, raw)
           VALUES ($1, $2, $3, $4, $5, $6, $7::jsonb)
           ON CONFLICT (legacy_id) WHERE legacy_id <> '' DO UPDATE SET
             name = EXCLUDED.name,
             file_name = EXCLUDED.file_name,
             file_url = EXCLUDED.file_url,
             coin = EXCLUDED.coin,
             category = EXCLUDED.category,
             raw = EXCLUDED.raw`,
          [
            String(gift.id || ""),
            String(gift.name || ""),
            String(gift.file || ""),
            normalizePath(gift.file, "/gifts_git"),
            Number(gift.coin || 0),
            String(gift.category || ""),
            gift,
          ],
        );
        gifts += 1;
      }
    }

    roomBadges += await normalizeBadges(client, "room_badges", "settings.room_badges", row.room_badges);
    siteBadges += await normalizeBadges(client, "site_badges", "settings.site_badges", row.site_badges);
  }

  return { settings, assets, categories, gifts, siteBadges, roomBadges };
}

async function normalizeBadges(client, table, source, value) {
  const badges = parseJson(value, []);
  if (!Array.isArray(badges)) return 0;
  let count = 0;
  for (const badge of badges) {
    await insertJson(
      client,
      `INSERT INTO ${table} (legacy_id, name, background, icon_url, background_type, raw)
       VALUES ($1, $2, $3, $4, $5, $6::jsonb)
       ON CONFLICT (legacy_id) WHERE legacy_id <> '' DO UPDATE SET
         name = EXCLUDED.name,
         background = EXCLUDED.background,
         icon_url = EXCLUDED.icon_url,
         background_type = EXCLUDED.background_type,
         raw = EXCLUDED.raw`,
      [
        String(badge.id || ""),
        String(badge.name || ""),
        String(badge.bg || ""),
        String(badge.icon || ""),
        String(badge.bg_type || "static"),
        { source, ...badge },
      ],
    );
    count += 1;
  }
  return count;
}

async function normalizePowers(client) {
  const rows = (await client.query("SELECT powers FROM powers ORDER BY id")).rows;
  let roles = 0;
  let permissions = 0;
  let assignments = 0;
  const roleIds = new Map();

  for (const row of rows) {
    const items = parseJson(row.powers, []);
    if (!Array.isArray(items)) continue;
    for (const role of items) {
      const name = String(role.name || "");
      if (!name) continue;
      const inserted = await insertJson(
        client,
        `INSERT INTO roles (legacy_name, name, rank, icon_url, auto_enabled, auto_promote, auto_points, raw)
         VALUES ($1, $2, $3, $4, $5, $6, $7, $8::jsonb)
         ON CONFLICT (legacy_name) DO UPDATE SET
           name = EXCLUDED.name,
           rank = EXCLUDED.rank,
           icon_url = EXCLUDED.icon_url,
           auto_enabled = EXCLUDED.auto_enabled,
           auto_promote = EXCLUDED.auto_promote,
           auto_points = EXCLUDED.auto_points,
           raw = EXCLUDED.raw
         RETURNING id`,
        [
          name,
          name,
          Number(role.rank || 0),
          normalizePath(role.ico, "/sico"),
          Number(role.auto_enabled || 0),
          Number(role.auto_promote || 0),
          Number(role.auto_points || 0),
          role,
        ],
      );
      const roleId = inserted.rows[0].id;
      roleIds.set(name, roleId);
      roles += 1;

      for (const [key, value] of Object.entries(role)) {
        if (["rank", "name", "ico"].includes(key)) continue;
        await insertJson(
          client,
          `INSERT INTO role_permissions (role_id, permission_key, permission_value)
           VALUES ($1, $2, $3::jsonb)
           ON CONFLICT (role_id, permission_key) DO UPDATE SET permission_value = EXCLUDED.permission_value`,
          [roleId, key, JSON.stringify(value)],
        );
        permissions += 1;
      }
    }
  }

  const users = (await client.query("SELECT idreg, power FROM users WHERE power IS NOT NULL AND power <> ''")).rows;
  for (const user of users) {
    const roleId = roleIds.get(user.power);
    if (!roleId) continue;
    await insertJson(
      client,
      `INSERT INTO user_role_assignments (user_idreg, role_id, power_name, raw)
       VALUES ($1, $2, $3, $4::jsonb)
       ON CONFLICT (user_idreg, role_id) DO UPDATE SET power_name = EXCLUDED.power_name, raw = EXCLUDED.raw`,
      [user.idreg, roleId, String(user.power || ""), { source: "users.power" }],
    );
    assignments += 1;
  }

  return { roles, permissions, assignments };
}

